#include <iostream>
#include <vector>

using namespace std;

// Source: https://www.hackerrank.com/challenges/between-two-sets

int main() {
  int n, m;
  cin >> n >> m;
  vector<int>, a(n), b(m);

  for (int i = 0; i < a.size(); ++i) {
    cin >> a[i];
  }

  for (int i = 0; i < b.size(); ++i) {
    cin >> b[i];
  }

  return 0;
}
